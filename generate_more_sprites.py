import os
from PIL import Image

def str_to_img(frame_str, palette):
    lines = frame_str.strip('\n').split('\n')
    img = Image.new('RGBA', (16, 16), (0,0,0,0))
    pixels = img.load()
    for y, line in enumerate(lines):
        padded = line.ljust(16, '.')
        for x, char in enumerate(padded):
            if char in palette:
                pixels[x, y] = palette[char]
    return img

def make_spritesheet(name, frames_strs, palette, scale=3):
    sheet = Image.new('RGBA', (16 * 4, 16), (0,0,0,0))
    for i, f_str in enumerate(frames_strs):
        frame_img = str_to_img(f_str, palette)
        sheet.paste(frame_img, (i * 16, 0))
    sheet = sheet.resize((16 * 4 * scale, 16 * scale), Image.NEAREST)
    sheet.save(f"/home/jason/.openclaw/workspace/star-office-ui/frontend/{name}.png")

