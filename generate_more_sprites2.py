import os
from PIL import Image

def str_to_img(frame_str, palette):
    lines = [l for l in frame_str.strip('\n').split('\n') if l]
    h = len(lines)
    w = max(len(l) for l in lines)
    img = Image.new('RGBA', (w, h), (0,0,0,0))
    pixels = img.load()
    for y, line in enumerate(lines):
        for x, char in enumerate(line):
            if char in palette:
                pixels[x, y] = palette[char]
    return img.resize((48, 48), Image.NEAREST)

def make_spritesheet(name, frames_strs, palette):
    sheet = Image.new('RGBA', (48 * 4, 48), (0,0,0,0))
    for i, f_str in enumerate(frames_strs):
        frame_img = str_to_img(f_str, palette)
        sheet.paste(frame_img, (i * 48, 0))
    sheet.save(f"/home/jason/.openclaw/workspace/star-office-ui/frontend/{name}.png")

